//*  While Loop

let index = 0
while (index <= 10) {
    // console.log(`Value of index is ${index}`);
    index = index + 2
}

let myArray = ['Suyash', 'Shikhar', 'Arun', 'Shraddha'];
let arr = 0
while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr++
}


//* Do While Loop
//? It checks condition before and runs the loop after
let score = 11
do {
    console.log(`Value of score is ${score} by doWhile Loop`);
    score++;
} while (score < 10);